#define P_GPIO_DAC_RST_N        (1 << 1)
#define P_GPIO_ADC_RST_N          (1 << 2)
#define P_GPIO_USB_SEL0         (1 << 3)
#define P_GPIO_USB_SEL1         (1 << 4)
#define P_GPIO_VBUS_EN          (1 << 5)
#define P_GPIO_MCLK_FSEL        (1 << 6)
#define P_GPIO_PLL_SEL          (1 << 7)



